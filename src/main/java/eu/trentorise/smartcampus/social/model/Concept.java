package eu.trentorise.smartcampus.social.model;

import java.io.Serializable;


/**
 * Generic representation of the tag. May define textual tag or semantic tag. In the latter case 
 * the {@link #id} field will correspond to the semantic concept id, while {@link #name}, {@link #description}, and {@link #setSummary(String)}
 * will represent the label, description and summary of the concept respectively. 
 * @author raman
 *
 */
public class Concept implements Serializable {
	private static final long serialVersionUID = -5336319375263642193L;

	private String id;
	private String name;
	private String description;
	private String summary;

	public Concept(String id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public Concept() {
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Concept other = (Concept) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}
	
	
}
